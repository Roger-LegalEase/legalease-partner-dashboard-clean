import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const compiledRoot = path.join(root, "src/lib/rcap-engine/compiled");
const publicProfilesPath = path.join(compiledRoot, "all51.json");
const engineProfilesRoot = path.join(compiledRoot, "profiles");
const reportPath = path.join(root, "docs/expungement-ai/SCREENING_FRICTION_AUDIT.md");

const EXPECTED_JURISDICTION_COUNT = 51;
const SOURCE_QUESTION_PREFIX = "source_question";

const packetPreparationIds = new Set([
  "court",
  "county",
  "county_or_filing_location",
  "case_identifier",
  "case_number",
  "charge",
  "record_documents",
  "criminal_history"
]);

const reviewCautionIds = new Set([
  "possible_pathway_context",
  "prior_relief"
]);

const eligibilityIds = new Set([
  "ownership_scope",
  "jurisdiction_scope",
  "case_outcome",
  "offense_level",
  "sentence_completion_date",
  "financial_obligations",
  "pending_cases",
  "disposition_date",
  "age_at_offense",
  "pardon_status",
  "state_exclusion_categories",
  "trafficking_status",
  "identity_error",
  "arrest_date"
]);

const reportHeader = `# Expungement.ai Screening Friction Audit

Generated by \`npm run expungement:audit-screening-friction\`.

Scope: audit only. This report inspects the current compiled public profile projection and engine profile metadata. It does not change runtime behavior, evaluator behavior, payment behavior, UI behavior, profile JSON, route logic, Wilma, payment code, or migrations.
`;

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, "utf8"));
}

function deriveScreens(profile) {
  const stageOrder = new Map(profile.flowStages.map((stage) => [stage.id, stage.order]));
  return profile.questions
    .map((question, index) => ({ question, index }))
    .filter(({ question }) => !question.id.startsWith(SOURCE_QUESTION_PREFIX))
    .sort((a, b) => {
      const orderA = stageOrder.get(a.question.stage) ?? Number.MAX_SAFE_INTEGER;
      const orderB = stageOrder.get(b.question.stage) ?? Number.MAX_SAFE_INTEGER;
      return orderA - orderB || a.index - b.index;
    })
    .map(({ question }) => question);
}

function classifyQuestion(question) {
  if (packetPreparationIds.has(question.id)) return "packet-preparation";
  if (reviewCautionIds.has(question.id)) return "review-caution";
  if (eligibilityIds.has(question.id)) return "eligibility-decision";

  const haystack = `${question.id} ${question.stage} ${question.prompt}`.toLowerCase();
  if (/(court|county|case number|case identifier|docket|charge|statute|document|criminal-history|certified disposition)/.test(haystack)) {
    return "packet-preparation";
  }
  if (/(not sure|uncertain|verify|close to your situation|prior relief)/.test(haystack)) {
    return "review-caution";
  }
  if (/(eligible|pending|sentence|supervision|probation|parole|waiting|dismiss|conviction|federal|state or local|exclusion|pardon|trafficking|identity|arrest date)/.test(haystack)) {
    return "eligibility-decision";
  }
  return "unclear";
}

function reasonFor(question, classification) {
  if (classification === "packet-preparation") {
    if (question.id === "record_documents" || question.id === "criminal_history") {
      return "Record/document readiness is important for packet assembly and filing verification, but it looks more like post-payment fulfillment unless the answer is being used as a true source-defined eligibility blocker.";
    }
    if (question.id === "charge") {
      return "Exact charge/statute detail is usually needed to fill and verify a packet after a pathway is otherwise clear.";
    }
    if (question.id === "court" || question.id === "county" || question.id === "county_or_filing_location") {
      return "Court or filing location is generally a form-routing and filing-logistics detail.";
    }
    return "This looks like case-detail collection needed to assemble a packet, not a standalone eligibility or pathway fact.";
  }
  if (classification === "review-caution") {
    if (question.id === "possible_pathway_context") {
      return "The current profile marks this as context-only and does-not-select-pathway, so it is a candidate for a later optional/collapsed or post-payment treatment if tests preserve evaluator outcomes.";
    }
    if (question.id === "prior_relief") {
      return "Prior relief currently drives caution semantics rather than a direct exclusion in the evaluator; any change still needs source review before relaxing it.";
    }
    return "This may be better represented as packet_ready_with_caution only where source logic confirms no hard stop, exclusion, timing block, or missing pathway fact.";
  }
  if (classification === "eligibility-decision") {
    return "This is used to determine scope, pathway, timing, exclusion, or result code and should remain pre-payment unless source logic is rewritten and re-reviewed.";
  }
  return "Unclear classification. Treat as pre-payment until source review proves otherwise.";
}

function riskFor(profile, questions) {
  const packetCount = questions.filter((question) => question.classification === "packet-preparation").length;
  const cautionCount = questions.filter((question) => question.classification === "review-caution").length;
  const exclusionCount = questions.filter((question) => question.id === "state_exclusion_categories").length;
  const specialCount = questions.filter((question) => ["pardon_status", "trafficking_status", "identity_error", "prior_relief", "age_at_offense"].includes(question.id)).length;
  const automaticRoutes = (profile.packetGenerator?.pathways ?? []).filter((pathway) => pathway.mode === "automatic_relief_verification_and_guidance").length;

  if (exclusionCount > 0 || automaticRoutes > 0 || specialCount >= 4 || cautionCount > 1) return "high";
  if (specialCount >= 2 || packetCount >= 5 || questions.length >= 17) return "medium";
  return "low";
}

function conversionUpsideFor(packetCount, cautionCount, visibleCount) {
  const reducible = packetCount + cautionCount;
  if (reducible >= 5 || visibleCount >= 18) return "high";
  if (reducible >= 3) return "medium";
  return "low";
}

function paymentGateRiskFor(legalRisk, packetCount, cautionCount) {
  if (legalRisk === "high") return "high";
  if (legalRisk === "medium" || cautionCount > 0 || packetCount >= 5) return "medium";
  return "low";
}

function recommendedActionFor(legalRisk, packetCount, cautionCount) {
  if (packetCount === 0 && cautionCount === 0) return "no change";
  if (legalRisk === "high") return "attorney review first";
  if (cautionCount > 0) return "source review first";
  return "implement later";
}

function mdList(items, fallback = "None.") {
  if (!items.length) return fallback;
  return items.map((item) => `- ${item}`).join("\n");
}

function compactList(rows) {
  return rows.map((row, index) => `${index + 1}. ${row}`).join("\n");
}

function table(rows) {
  return rows.join("\n");
}

function main() {
  const publicProfiles = readJson(publicProfilesPath);
  const engineFiles = fs.readdirSync(engineProfilesRoot).filter((file) => file.endsWith(".json")).sort();
  const engineProfiles = new Map(engineFiles.map((file) => {
    const profile = readJson(path.join(engineProfilesRoot, file));
    return [profile.jurisdiction.code, profile];
  }));

  const codes = Object.keys(publicProfiles).sort();
  if (codes.length !== EXPECTED_JURISDICTION_COUNT) {
    throw new Error(`Expected ${EXPECTED_JURISDICTION_COUNT} public profiles, found ${codes.length}.`);
  }
  if (engineProfiles.size !== EXPECTED_JURISDICTION_COUNT) {
    throw new Error(`Expected ${EXPECTED_JURISDICTION_COUNT} engine profiles, found ${engineProfiles.size}.`);
  }

  const jurisdictions = codes.map((code) => {
    const publicProfile = publicProfiles[code];
    const engineProfile = engineProfiles.get(code);
    if (!engineProfile) throw new Error(`Missing engine profile for ${code}.`);

    const visibleQuestions = deriveScreens(publicProfile).map((question) => {
      const classification = classifyQuestion(question);
      return {
        ...question,
        classification,
        reason: reasonFor(question, classification)
      };
    });

    const packetCandidates = visibleQuestions.filter((question) => question.classification === "packet-preparation");
    const cautionCandidates = visibleQuestions.filter((question) => question.classification === "review-caution");
    const mustRemain = visibleQuestions.filter((question) => question.classification === "eligibility-decision" || question.classification === "unclear");
    const legalRisk = riskFor(engineProfile, visibleQuestions);
    const conversionUpside = conversionUpsideFor(packetCandidates.length, cautionCandidates.length, visibleQuestions.length);
    const paymentGateRisk = paymentGateRiskFor(legalRisk, packetCandidates.length, cautionCandidates.length);
    const recommendedAction = recommendedActionFor(legalRisk, packetCandidates.length, cautionCandidates.length);

    return {
      code,
      name: publicProfile.jurisdiction.name,
      visibleCount: visibleQuestions.length,
      questions: visibleQuestions,
      packetCandidates,
      cautionCandidates,
      mustRemain,
      legalRisk,
      conversionUpside,
      paymentGateRisk,
      recommendedAction,
      reducibleCount: packetCandidates.length + cautionCandidates.length
    };
  });

  const totalVisible = jurisdictions.reduce((sum, jurisdiction) => sum + jurisdiction.visibleCount, 0);
  const totalReducible = jurisdictions.reduce((sum, jurisdiction) => sum + jurisdiction.reducibleCount, 0);
  const averageVisible = totalVisible / jurisdictions.length;
  const riskCounts = jurisdictions.reduce((counts, jurisdiction) => {
    counts[jurisdiction.legalRisk] += 1;
    return counts;
  }, { low: 0, medium: 0, high: 0 });

  const frictionHeavy = [...jurisdictions]
    .sort((a, b) => b.visibleCount - a.visibleCount || b.reducibleCount - a.reducibleCount || a.name.localeCompare(b.name))
    .slice(0, 10);

  const safeCandidates = [...jurisdictions]
    .filter((jurisdiction) => jurisdiction.legalRisk === "low" || jurisdiction.legalRisk === "medium")
    .sort((a, b) => {
      const riskScore = { low: 0, medium: 1, high: 2 };
      const gateScore = { low: 0, medium: 1, high: 2 };
      const upsideScore = { high: 0, medium: 1, low: 2 };
      return gateScore[a.paymentGateRisk] - gateScore[b.paymentGateRisk]
        || upsideScore[a.conversionUpside] - upsideScore[b.conversionUpside]
        || b.reducibleCount - a.reducibleCount
        || riskScore[a.legalRisk] - riskScore[b.legalRisk]
        || a.name.localeCompare(b.name);
    })
    .slice(0, 10);

  const highRisk = jurisdictions.filter((jurisdiction) => jurisdiction.legalRisk === "high")
    .sort((a, b) => b.visibleCount - a.visibleCount || a.name.localeCompare(b.name));

  const sections = [];
  sections.push(reportHeader);
  sections.push(`## Executive summary

- Total jurisdictions reviewed: ${jurisdictions.length}
- Current average visible pre-payment question count: ${averageVisible.toFixed(1)}
- Estimated reducible question count: ${totalReducible}
- Low-risk jurisdictions: ${riskCounts.low}
- Medium-risk jurisdictions: ${riskCounts.medium}
- High-risk jurisdictions: ${riskCounts.high}

Top 10 friction-heavy states:
${compactList(frictionHeavy.map((jurisdiction) => `${jurisdiction.code} ${jurisdiction.name}: ${jurisdiction.visibleCount} visible questions, ${jurisdiction.reducibleCount} candidate reducible questions, ${jurisdiction.legalRisk} legal/source risk`))}

Top 10 safest conversion-improvement candidates:
${compactList(safeCandidates.map((jurisdiction) => `${jurisdiction.code} ${jurisdiction.name}: ${jurisdiction.reducibleCount} candidate reducible questions, ${jurisdiction.conversionUpside} upside, ${jurisdiction.paymentGateRisk} payment-gate risk`))}
`);

  sections.push(`## Method

The audit mirrors the current consumer screen derivation: sort profile questions by \`flowStages\` stage order and original profile order, and exclude only \`${SOURCE_QUESTION_PREFIX}_*\` rows. It reads \`src/lib/rcap-engine/compiled/all51.json\` as the current designer public projection source and cross-checks \`src/lib/rcap-engine/compiled/profiles/*.json\` to ensure all 51 engine profiles are present.

Classification is conservative. A question is only proposed for later post-payment movement when its id/prompt looks like court, county, case number, charge, document, or filing-logistics collection. Review/caution candidates are not recommendations to allow payment; they are candidates for later source-reviewed caution handling only when all hard stops, timing blocks, exclusions, automatic/no-filing routes, and true pathway facts remain satisfied.
`);

  sections.push(`## Jurisdiction detail`);

  for (const jurisdiction of jurisdictions) {
    sections.push(`### ${jurisdiction.code} - ${jurisdiction.name}

- Current visible pre-payment question count: ${jurisdiction.visibleCount}
- Legal/source-risk level: ${jurisdiction.legalRisk}
- Conversion upside: ${jurisdiction.conversionUpside}
- Payment-gate risk: ${jurisdiction.paymentGateRisk}
- Recommended action: ${jurisdiction.recommendedAction}

Questions:
${mdList(jurisdiction.questions.map((question) => `\`${question.id}\` (${question.classification}): ${question.prompt}`))}

Proposed post-payment candidates:
${mdList(jurisdiction.packetCandidates.map((question) => `\`${question.id}\`: ${question.reason}`))}

Proposed caution candidates:
${mdList(jurisdiction.cautionCandidates.map((question) => `\`${question.id}\`: ${question.reason}`))}

Questions that must remain pre-payment:
${mdList(jurisdiction.mustRemain.map((question) => `\`${question.id}\`: ${question.reason}`))}
`);
  }

  sections.push(`## Guardrails

Payment must remain blocked for \`hard_stop\`, \`guidance_only\`, \`not_yet\`, \`likely_not_eligible\`, \`needs_review\`, and missing eligibility facts.

- \`hard_stop\`: the matter is outside self-help state screening scope, such as not the user's own record or a federal/out-of-scope matter.
- \`guidance_only\`: the selected route is automatic, no-filing, or guidance-only, so charging for a filing packet would be unsafe and misleading.
- \`not_yet\`: a source-defined timing, waiting-period, completion, supervision, or pending-case condition is not satisfied.
- \`likely_not_eligible\`: the user selected a source-defined exclusion or disqualifying/review-required category.
- \`needs_review\`: the supplied facts are ambiguous, unknown, or do not match an executable source-defined route.
- Missing true eligibility or pathway facts: the evaluator currently returns \`needs_more_info\` for required public facts. Any future relaxation must distinguish packet-preparation details from facts that drive scope, pathway, exclusions, timing, automatic/no-filing routes, or legal result code.

Payment should remain allowed only when the evaluator reaches \`packet_ready\` or \`packet_ready_with_caution\` and the packet plan remains fulfillment-ready. This audit does not recommend payment for any other result code.
`);

  sections.push(`## Implementation proposal - do not implement in this pass

Safest low-risk files to modify later:
- \`src/lib/rcap-engine/public-profile-projection.ts\`, only if adding a non-runtime-breaking public metadata flag after tests prove evaluator parity.
- \`src/components/expungement-ai/screening/screens.ts\`, only if later introducing a source-reviewed optional/post-payment screen partition.
- \`src/components/expungement-ai/screening/ScreeningFlow.tsx\`, only if later changing when packet-preparation screens render while preserving evaluator submission requirements.
- \`src/components/expungement-ai/screening/ScreeningResult.tsx\`, only if later showing post-payment/caution follow-up copy without changing paymentAllowed.

Tests required before a behavior-changing phase:
- Golden evaluation tests for all 51 states proving resultCode, paymentAllowed, missingQuestionIds, pathwayId, and packetPlan are unchanged for existing complete-answer fixtures.
- New missing-answer tests proving true eligibility/pathway facts still block payment.
- Regression tests proving hard_stop, guidance_only, not_yet, likely_not_eligible, and needs_review never show checkout.
- UI tests proving only source-approved packet-preparation questions move after payment and no Stripe or checkout logic changes.

States to pilot first:
${mdList(safeCandidates.slice(0, 5).map((jurisdiction) => `${jurisdiction.code} ${jurisdiction.name}: ${jurisdiction.reducibleCount} candidate questions, ${jurisdiction.legalRisk} legal/source risk`))}

States not to touch without review:
${mdList(highRisk.slice(0, 15).map((jurisdiction) => `${jurisdiction.code} ${jurisdiction.name}: ${jurisdiction.visibleCount} questions, ${jurisdiction.reducibleCount} candidates, ${jurisdiction.legalRisk} legal/source risk`))}

Rollback plan:
- Keep the current all-pre-payment profile-driven flow behind the existing screen derivation.
- Ship any later behavior change behind a feature flag or state allowlist.
- Roll back by removing states from the allowlist or disabling the flag; do not change evaluator or paymentAllowed semantics during rollback.
- Preserve a verifier that compares old and new gate results before enabling any state.
`);

  sections.push(`## Revenue and conversion notes

The strongest relative conversion opportunity is in states with many visible questions where several are court, county, case identifier, charge, or record-document details. Those questions likely add effort before the user knows whether a paid packet pathway exists.

This audit does not fabricate revenue numbers. Relative upside appears highest where a later phase can reduce four or more packet-preparation/review-caution screens while leaving all hard stops, eligibility facts, timing facts, exclusion facts, and pathway-selection facts before payment. Upside is lower where most questions are true eligibility decisions or where source review is needed before any relaxation.
`);

  sections.push(`## Summary tables

Friction-heavy states:

${table([
  "| State | Visible questions | Candidate reducible | Legal/source risk | Conversion upside | Payment-gate risk |",
  "| --- | ---: | ---: | --- | --- | --- |",
  ...frictionHeavy.map((jurisdiction) => `| ${jurisdiction.code} ${jurisdiction.name} | ${jurisdiction.visibleCount} | ${jurisdiction.reducibleCount} | ${jurisdiction.legalRisk} | ${jurisdiction.conversionUpside} | ${jurisdiction.paymentGateRisk} |`)
])}

Safest candidates:

${table([
  "| State | Visible questions | Candidate reducible | Legal/source risk | Conversion upside | Recommended action |",
  "| --- | ---: | ---: | --- | --- | --- |",
  ...safeCandidates.map((jurisdiction) => `| ${jurisdiction.code} ${jurisdiction.name} | ${jurisdiction.visibleCount} | ${jurisdiction.reducibleCount} | ${jurisdiction.legalRisk} | ${jurisdiction.conversionUpside} | ${jurisdiction.recommendedAction} |`)
])}
`);

  fs.mkdirSync(path.dirname(reportPath), { recursive: true });
  fs.writeFileSync(reportPath, `${sections.join("\n\n").trim()}\n`);

  console.log("Expungement.ai screening friction audit complete.");
  console.log(`Jurisdictions reviewed: ${jurisdictions.length}`);
  console.log(`Average visible pre-payment questions: ${averageVisible.toFixed(1)}`);
  console.log(`Estimated reducible questions: ${totalReducible}`);
  console.log(`Risk counts: low ${riskCounts.low}, medium ${riskCounts.medium}, high ${riskCounts.high}`);
  console.log(`Report: ${path.relative(root, reportPath)}`);
}

main();
